#addizione

addizione = 3 + 2
print("3 + 2 = ", addizione)

#sottrazione

sottrazione = 3 - 2
print("3 - 2 = ", sottrazione)

#moltiplicazione

prodotto = 3 * 2
print("3 x 2 = ", prodotto)

#divisione

rapporto = 3/2
print("3 / 2 = ", rapporto)

#esponenziale

risultato = 3 ** 2
print("3^2 = ", risultato)

#floor division (restituisce il risultato intero di un rapporto arrotondando per difetto)

f_division = 3 // 2
print("3/2 (arrotondato per difetto) = ", f_division)

#modulo (restituisce il resto di una divisione, ossia la quantità intera da aggiungere al prodotto fra il divisore e il risultato intero per ritornare al dividendo originale)

modulo = 3 % 2
print("3/2 (intero) = ", f_division, "con resto ", modulo, ". Questo perché per avere 3, il dividendo, devo aggiungere al prodotto di 2 *",f_division, " una quantità ", modulo)